# Canción de Joaquín Sabina
SONG = """Lo nuestro duró
lo que duran dos peces de hielo
en un güisqui on the rocks,
en vez de fingir,
o estrellarme una copa de celos,
le dio por reír.
De pronto me vi,
como un perro de nadie,
ladrando, a las puertas del cielo.
Me dejó un neceser con agravios,
la miel en los labios
y escarcha en el pelo.
Tenían razón
mis amantes
en eso de que, antes,
el malo era yo,
con una excepción:
esta vez,
yo quería quererla querer
y ella no.
Así que se fue,
me dejó el corazón
en los huesos
y yo de rodillas.
Desde el taxi,
y, haciendo un exceso,
me tiró dos besos...
uno por mejilla.
Y regresé
a la maldición
del cajón sin su ropa,
a la perdición
de los bares de copas,
a las cenicientas
de saldo y esquina,
y, por esas ventas
del fino Laína,
pagando las cuentas
de gente sin alma
que pierde la calma
con la cocaína,
volviéndome loco,
derrochando
la bolsa y la vida
la fui, poco a poco,
dando por perdida.
Y eso que yo,
para no agobiar con
flores a María,
para no asediarla
con mi antología
de sábanas frías
y alcobas vacías,
para no comprarla
con bisutería,
ni ser el fantoche
que va, en romería,
con la cofradía
del Santo Reproche,
tanto la quería,
que, tardé, en aprender
a olvidarla, diecinueve días
y quinientas noches.
Dijo hola y adiós,
y, el portazo, sonó
como un signo de interrogación,
sospecho que, así,
se vengaba, a través del olvido,
Cupido de mí.
No pido perdón,
¿para qué? si me va a perdonar
porque ya no le importa...
siempre tuvo la frente muy alta,
la lengua muy larga
y la falda muy corta.
Me abandonó,
como se abandonan
los zapatos viejos,
destrozó el cristal
de mis gafas de lejos,
sacó del espejo
su vivo retrato,
y, fui, tan torero,
por los callejones
del juego y el vino,
que, ayer, el portero,
me echó del casino
de Torrelodones.
Qué pena tan grande,
negaría el Santo Sacramento,
en el mismo momento
que ella me lo mande.
Y eso que yo,
para no agobiar con
flores a María,
para no asediarla
con mi antología
de sábanas frías
y alcobas vacías,
para no comprarla
con bisutería,
ni ser el fantoche
que va, en romería,
con la cofradía
del Santo Reproche,
tanto la quería,
que, tardé, en aprender
a olvidarla, diecinueve días
y quinientas noches."""